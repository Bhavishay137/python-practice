# Program demonstrating for loop with different data structures

# List iteration
li = ["geeks", "for", "geeks"]
for x in li:
    print(x)

# Tuple iteration
tup = ("geeks", "for", "geeks")
for x in tup:
    print(x)

# String iteration
s = "abc"
for x in s:
    print(x)

# Dictionary iteration
d = {'x': 123, 'y': 354}
for x in d:
    print("%s  %d" % (x, d[x]))

# Set iteration
set1 = {10, 30, 20}
for x in set1:
    print(x)